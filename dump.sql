USE DB;

CREATE TABLE IF NOT EXISTS `MESSAGES_REQUEST` (
  `ID` MEDIUMINT NOT NULL AUTO_INCREMENT,
  `COR_ID` varchar(36) NOT NULL UNIQUE,
  `SERVICE_ADDRESS` varchar(36) NOT NULL,
  `HOST` varchar(36) NOT NULL,
  `MSG` longtext NOT NULL,
  `DT_CREATION` datetime DEFAULT CURRENT_TIMESTAMP NOT NULL,
  `APP_NAME` varchar(36) NOT NULL,
  `CHECKSUM` varchar(36) NOT NULL,
  PRIMARY KEY (`ID`)

) DEFAULT CHARSET=utf8mb3;

ALTER TABLE `MESSAGES_REQUEST`
  ADD KEY `INDEX_DT_CREATION` (`DT_CREATION`),
  ADD KEY `INDEX_CHECKSUM` (`CHECKSUM`),
  ADD KEY `IDX_COR_ID` (`COR_ID`);


